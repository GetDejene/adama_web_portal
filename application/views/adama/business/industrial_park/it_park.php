                                                <div style="text-align: justify;">
                                                    <div> Building an ecosystem for IT-led growth and development through synergistic linkages between the industry, government and academia.</div>
                                                    <div> &nbsp;</div>
                                                    <div> The government has envisioned a holistic growth plan for the state with the hub-and-spoke model of development. Technopark (Thiruvananthapuram), Infopark (Kochi) and Cyberpark (Kozhikode) act as hubs for satellite centres in the hinterland.</div>
                                                    <div> &nbsp;</div>
                                                    <div> Technopark, Thiruvananthapuram: It is one of India's largest IT parks and one of world's greenest technopolis as well. It is Kerala's first IT park with more than 410 companies employing more than 60000 IT professionals.&nbsp;</div>
                                                    <div> &nbsp;</div>
                                                    <div> For more information, visit:&nbsp; <a href="http://www.technopark.org/" target="_blank">www.technopark.org</a></div>
                                                    <div> &nbsp;</div>
                                                    <div> Infopark, Kochi: Ever since its inception in 2004, Infopark has been growing fast and attracting investments from IT majors. The 260-acre campus is divided into Special Economic Zone (SEZ) and non-SEZ facilities.</div>
                                                    <div> &nbsp;</div>
                                                    <div> For more information, visit:&nbsp; <a href="http://infopark.in/" target="_blank">infopark.in</a></div>
                                                    <div> &nbsp;</div>
                                                    <div> Cyberpark, Kozhikode: Cyberpark offers tailor-made workspaces, intelligent infrastructure and end-to-end solutions for global IT/ITeS companies. It is strategically planned, road-mapped and fast-developing as the ideal destination for investments in the information technology sector.&nbsp;</div>
                                                    <div> &nbsp;</div>
                                                    <div> For more information, visit:&nbsp; <a href="http://www.cyberparkkerala.org/" target="_top">www.cyberparkkerala.org</a></div>
                                                    <div> &nbsp;</div>
                                                    <div> The three IT parks together have more than 800 companies that provide employment to over&nbsp; 1 lakh IT professionals. Technopark is extensively laid out across 724 acres of land in 5 phases, with 9.7 million sq.ft.of built-up space. Has both IT SEZ and DTA areas within the campus. Infopark has a total built-up area of 6 million sq.ft.and Cyberpark a combined built-up area of 8 Lakhs sq.ft. An additional space of 10 million sq.ft. is getting targeted by 2021 in the IT parks to meet the growing demand from companies.</div>
                                                    <div> &nbsp;</div>
                                                    <div> Source: <a href="http://www.keralait.org/industry" target="_blank">www.keralait.org/</a></div>

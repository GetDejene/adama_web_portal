                                                    <div>
                                                        <p> <b>Solar Energy Programmes</b></p>
                                                        <div style="text-align: justify;"> &nbsp;</div>
                                                        <div style="text-align: justify;"> Solar thermal (utilizing heat from the sun) and solar photovoltaic (generating electricity from sunlight) programmes are implemented by ANERT.</div>
                                                        <div> &nbsp;</div>
                                                    </div>
                                                    <div> <button class="accordion">Solar Photovoltaic Programme</button>
                                                        <div class="panel">
                                                            <div style="text-align:justify">
                                                                <div> &nbsp;</div> <b>Under this program, ANERT had distributed the following:</b>
                                                                <div>
                                                                    <ul>
                                                                        <li> Solar PV devices like solar lantern (64000)</li>
                                                                        <li> Solar home lighting systems (7900)</li>
                                                                        <li> Solar street lighting systems (712)</li>
                                                                        <li> Solar TV power pack (150)</li>
                                                                        <li> Solar water pumping systems (deep &amp; shallow well)</li>
                                                                        <li> Solar fencing energizers (10)</li>
                                                                        <li> Solar vaccine refrigerator (10)</li>
                                                                        <li> Solar fishermen pack (25)</li>
                                                                        <li> SPV demo kits for schools (350)</li>
                                                                        <li> PV insect light traps(4)</li>
                                                                        <li> PV rubber tapper's light(120)</li>
                                                                        <li> PV modules of 35,70 and 90W (7500) etc.</li>
                                                                    </ul>
                                                                </div>
                                                                <div> &nbsp;</div> Though only a few devices have MNRE subsidy, all the above devices were distributed to the public with a subsidy component utilizing ANERT funds. <div> &nbsp;</div> Currently off-grid and grid-tied solar photovoltaic power plants are also being promoted in a big way. <div> &nbsp;</div>
                                                            </div>
                                                        </div>
                                                        <div> <button class="accordion">Solar Thermal Programme</button>
                                                            <div class="panel">
                                                                <div style="text-align:justify">
                                                                    <div> &nbsp;</div> Solar Thermal Energy Programme of ANERT is aimed at supplementing thermal energy requirements at various temperature for different applications like cooking, water heating, Industrial process heating, crop drying, space heating, water desalination, etc. by harnessing solar energy and converting it into heat using various solar thermal devices and systems. <div> &nbsp;</div> ANERT has installed 1,24,000 lpd solar water heating systems in hospitals. So far, ANERT has installed 27626 m2 collector area in Kerala. With these installations, the power conserved is equivalent to 0.84 MW of conventional electricity generation capacity. <div> &nbsp;</div> Apart from this, ANERT has distributed 940 solar cookers, 15 solar crop driers, 3 industrial crop driers, 80 solar stills, 3 SK-14 cookers, 90 m2 of swimming pool heaters and has carried out revitalization of 10 old industrial systems. <div> <a class="three" href="http://www.anert.gov.in/" target="_blank">Click here</a></div>
                                                                    <div> &nbsp;</div>
                                                                </div>
                                                            </div>
                                                            <div> <button class="accordion">Micro-hydel Programme</button>
                                                                <div class="panel">
                                                                    <div style="text-align:justify">
                                                                        <div> &nbsp;</div> <b>Small Hydro Programme:</b> The State of Kerala has a large untapped potential for Small Hydro Power generation. ANERT has identified this as a thrust area of activity for the coming years and has initiated activities to mobilize potential developers of SHP in the State. <div> &nbsp;</div> Orientation training for selected Local Self Government Institutions, who are the possible developers of standalone SHP projects in remote rural areas, was conducted. A detailed training on technical aspects of SHP development was also organised to equip ANERT Engineers by Alternate Hydro Energy Centre, IIT Roorkee. <div> &nbsp;</div> Recently another 2 day program on 'water mills' was conducted for the Panchayat &amp; ANERT officials selected from identified areas where they can be used. The target for power generation from SHP sector in 11th plan period is 107 MW. Two micro hydel plants of 10 kw each have been completed by ANERT at Wayanad district. <div> &nbsp;</div> <b>Small hydro potential assessed by ANERT:</b> An extensive survey of small hydro sites have been undertaken by ANERT in 13 districts leaving out Alappuzha. The survey assumes significance since small hydro power generation is one among the major renewable energy responsibilities passed on to local governments and the study itself has been carried out jointly with local governments. <div> &nbsp;</div> <b>Cluster mode development of Small hydro projects:</b> Based on the small hydro survey, ANERT has initiated cluster mode development of 14 SHP sites in Peravoor block of Kannur district with the support of NABARD. <div> &nbsp;</div>
                                                                    </div>
                                                                </div>
                                                                <div> <button class="accordion">Wind Energy Programme</button>
                                                                    <div class="panel">
                                                                        <div style="text-align:justify">
                                                                            <div> &nbsp;</div> ANERT in association with MNRE had conducted a detailed study of the wind potential of Kerala and this is estimated to be about 605 MW. Even though Kerala is blessed with such a high wind potential, the State could not harness it fully for its effective utilization. <div> <a class="three" href="http://www.anert.gov.in/index.php?option=com_content&amp;view=article&amp;id=13&amp;Itemid=18" target="_blank">Click here</a></div>
                                                                            <div> &nbsp;</div>
                                                                        </div>
                                                                    </div>
                                                                    <div> <button class="accordion">Biogas Programme</button>
                                                                        <div class="panel">
                                                                            <div style="text-align:justify">
                                                                                <div> &nbsp;</div> This program is aimed to recover energy from waste; scientific disposal of waste; and conversion of waste into fertilizer after energy extraction; to improve sanitation; to protect environment; and also to generate employment opportunities. <div> &nbsp;</div> Biogas programmes are implemented by ANERT with financial assistance from MNRE and with active participation of beneficiaries. ANERT has installed 102 large size biogas plants since its inception with central financial support. An average of 10 lakh m3 bio-gas is being generated every year. These plants are installed at medical colleges, district medical hospitals, private and charitable institutions with 15 to 70 m3 capacity for thermal applications and electricity generation. <div> &nbsp;</div> Of the 102 plants, 73 use night soil, 24 use canteen wastes and 5 use animal waste as feed material. 5 plants generate electricity, each using a 10 kW dual-fuel engine. Other plants use biogas for heating applications. <div> <a class="three" href="http://www.anert.gov.in/index.php?option=com_content&amp;view=article&amp;id=10&amp;Itemid=22" target="_blank">Click here</a></div>

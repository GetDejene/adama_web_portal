                                                                        <p> Kerala Financial Corporation (KFC) incorporated under the State Financial Corporations Act of 1951, is a trend setter and path breaker in the field of long term finance, playing a major role in the development and industrialization of Kerala. It was established as the Travancore Cochin Financial Corporation on 01.12.1953. Consequent to the reorganization of states on linguistic basis in November 1956, Kerala State was formed and the Travancore Cochin Financial Corporation was renamed as Kerala Financial Corporation. The provisions of SFCs Act 1951 as amended in 2000 control and guide the functions of Corporation.</p>

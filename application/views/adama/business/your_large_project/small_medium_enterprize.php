                                                                    <div>
                                                                        <p> Kerala Financial Corporation (KFC) is the first PSU in Kerala and the first SFC in India to initiate Corporate Social Responsibility activity. As part of its Corporate Social Responsibility, KFC has set up KFC-CARE (Centre for Assistance and Rehabilitation) to rehabilitate and serve the marginalized sections of the community. The main objective of KFC is the rapid industrialization of the state by extending financial assistance to Micro, Small and Medium Enterprises in manufacturing and service sector. SFCs Act empowers KFC to formulate suitable loan schemes for achieving the above said objectives. Corporation can give financial assistance for setting up of new units and for the expansion / modernization / diversification of existing units in both manufacturing and service sectors.</p>

                                                            <p align="center"> &nbsp;</p>
                                                            <div> Ease of Doing Business (EoDB), an index published by the World Bank is a benchmark study of the regulations across different Countries of the World. It is an aggregate figure that includes different parameters which define the Ease of Doing Business in a country.</div>
                                                            <div> &nbsp;</div>
                                                            <div> The index is a means of measuring business regulations and its implementation across 190 economies and selected cities at the subnational and regional level. The Doing Business project, launched in 2002, looks at domestic small and medium-size companies and measures the regulations applying to them through their life cycle. It provides indicators on regulation for starting a business, dealing with construction permits, getting electricity, registering property, getting credit, protecting minority investors, paying taxes, trading across borders, enforcing contracts and resolving insolvency.</div>
                                                            <div> &nbsp;</div>
                                                            <div> According to the World Bank's latest â€˜Doing Business' index 2020, India stands at 63 out of 190 countries in the Ease of Doing Business ranking.&nbsp;</div>
                                                            <div> &nbsp;</div>
                                                            <div> Department of Industrial Policy and Promotion (DIPP), GoI had laid emphasis on simplification and rationalization of the existing rules and introduction of information technology to make governance more efficient, effective, simple and user-friendly. Govt. of India (GoI) has been ranking States based on the Ease of Doing Business and also in getting timely clearances. The States had been ranked based on the parameters that include ease in setting up a business, registration of property, labour compliances, infrastructure availability, finance and tax issues, inspection reforms and ease in exiting business. Kerala is in the 21st position in the EoDB ranking of States done by DIPP, GoI during the year 2017-'18.</div>
                                                            <div> &nbsp;</div>
                                                            <div> Government of Kerala (GoK) had decided to undertake reforms to help place the State on an equal footing amongst other top ranked States and identified specific areas for improvement in various aspects of doing business in the State. The key areas identified were i) Registration, ii) Revenue iii) Municipality Building Rules iv) Panchayat Building Rules v) Survey and Land Records, where speedy reformations required for making a more conducive atmosphere for starting business. It was also decided to adopt the best practices of other States to improve the ease of doing business environment in the State.</div>
                                                            <div> &nbsp;</div>
                                                            <div> KSIDC, being the nodal agency of the Government of Kerala, had done a study on the modification / amendments to be made in the concerned Rules and Regulations related to the existing clearance procedures in the State.</div>

                                                    <div style="text-align: justify;"> As part of improving land management activities in the State, Department of Revenue &amp; Disaster Management has constituted Institute of Land and Disaster Management.</div>
                                                    <div>
                                                        <div style="text-align: justify;"> &nbsp;</div>
                                                        <p style="text-align: justify;"> The Institute of Land and Disaster Management is an autonomous body under the Revenue department, Government of Kerala. A Division of the National Institute of Disaster Management (NIDM) also functions in the Institute. The Institute has been undertaking various activities including capacity building of the revenue and survey staff in Land management, and capacity building of stakeholders from various departments, NGOs and community in disaster management. The Institute functions under the overall control of the Governing body under the chairmanship of the Hon'ble Minister (Revenue) and the Executive Committee under the chairmanship of the Commissioner of Land Revenue, Government of Kerala.</p>
                                                    </div>
                                                    <div style="text-align: justify;"> &nbsp;</div>
                                                    <div style="text-align: justify;"> The Vision of the institute is sensitizing the needy, for bettering land revenue administration and disaster management.</div>
                                                    <div style="text-align: justify;"> &nbsp;</div>
                                                    <div style="text-align: justify;"> Its Mission include:</div>
                                                    <div style="text-align: justify;"> &nbsp;</div>
                                                    <div>
                                                        <ol>
                                                            <li style="text-align: justify;"> Strive continuously to improve knowledge, skill and attitude among government employees; especially revenue and survey departments.</li>
                                                            <li style="text-align: justify;"> Improve the existing administrative systems in land revenue and survey departments and to develop innovative mechanisms for bettering the service delivery to the citizens</li>
                                                            <li style="text-align: justify;"> Research and frame draft policies for improving the efficiency of revenue and survey administration and disaster management.</li>
                                                        </ol>

                                                                <div style="text-align:justify">
                                                                    <div> KSIDC offers financial assistance and support to medium and large scale industries in the State including service sectors like tourism, star category hotels, resorts, hospitals, infrastructure projects, etc and where the constitution of assisted unit is in the nature of Private/ Public Ltd Company. Normally financial assistance is available for projects with an investment of Rs. 200 Lakh and above. KSIDC's term loan assistance is limited upto a maximum of Rs. 3500 Lakh in a project and it can go up further for single project prior permission from State Government. The definition of the medium and large scale manufacturing industries, defined by the Central Government is applicable to KSIDC also.</div>
                                                                    <div> The standard norms for financial assistance of the projects:</div>
                                                                    <table border="1" cellpadding="25" cellspacing="1" style="width: 100%; height: 100%">

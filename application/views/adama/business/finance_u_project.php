                                                                            <tr>
                                                                                <td> &nbsp; <div> Repayment</div>
                                                                                </td>
                                                                                <td> &nbsp;Repayment period is 6-8 years with a moratorium of 2-3 years, on a selective basis.</td>
                                                                            </tr>
                                                                            <tr>
                                                                                <td> &nbsp; <div> Interest Rate</div>
                                                                                </td>
                                                                                <td> &nbsp;Based on a sliding scale with interest band ranging from 11.00 - 13.00%. Further, a further reduction of maximum up to 1% will be granted at the end of every financial year by reversal of the interest charged, provided there are no arrears of interest as on 31st March of the relevant particular year. The reduction in interest as above shall be calculated on the outstanding principal alone and there shall be no reversal of penal interest portion.</td>
                                                                            </tr>

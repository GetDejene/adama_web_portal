                                                                                    <p> &nbsp; a. 1:1 up to 1.5:1 for new companies with minimum/no track record.</p>
                                                                                    <p> &nbsp; b. 1.5:1 up to 2:1 for expansion of good assisted units &amp; other good non-assisted units.</p>
                                                                                </td>
                                                                            </tr>
                                                                            <tr>
                                                                                <td> &nbsp; <div> Term Loans</div>
                                                                                </td>
                                                                                <td> &nbsp;Up to Rs. 35 Crore can be sanctioned. No cap for term loan under direct financing. However loan exceeding Rs. 35 Crore is subject to approval by State Govt. The minimum amount considered for assistance is Rs.1 Crore except in case of expansion proposals of assisted units, where the minimum limit would be Rs. 50 lakhs.</td>
                                                                            </tr>
                                                                            <tr>
                                                                                <td> &nbsp; <div> Short Term Loan</div>
                                                                                </td>
                                                                                <td> &nbsp;May be considered for assisted units on a selective basis.</td>

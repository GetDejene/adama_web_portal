                                                            <div> KSIDC offers financial assistance and support to medium and large scale industries in the State including service sectors like tourism, star category hotels, resorts, hospitals, infrastructure projects etc and where the constitution of assisted unit is in the nature of Private/ Public Ltd Company.</div>
                                                            <div> Normally financial assistance is available for projects with an investment of Rs. 200 Lakh and above for general industry and Rs. 300 lakhs for Tourism, Hospital and Infrastructure projects. KSIDC's term loan assistance is limited upto a maximum of Rs. 3500 Lakh in a project and it can go up further for single project prior permission from State Government.</div>
                                                            <div> &nbsp;</div>
                                                            <div> The project should be financially and technically viable with satisfactory financial indices like Internal Rate of Return (IRR), Debt Service Coverage Ratio (DSCR), pay back period etc, and the promoter directors should execute personal guarantees with the Corporation for the timely repayment of the financial assistance sought.</div>

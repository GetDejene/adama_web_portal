                                                        <div> &nbsp;</div>
                                                        <div> <b>Early Stage Funding</b></div>
                                                        <div> Government of Kerala provides early-stage seed funding support to emerging start-ups through Kerala Start-up Mission and Kerala State Industries Development Corporation.</div>
                                                        <div> &nbsp;</div>
                                                        <div> Seed funding for start-ups began in 2006. At that time the process was not online and there were delays in the execution. Later in Sep 2015 Kerala Start-Up Mission launched its online portal and the applications are being received online. Since then, the frequency in executing the seed fund has increased.</div>
                                                        <div> &nbsp;</div>
                                                        <div> Kerala Start-Up Mission is providing seed support to the start-ups in the ecosystem by a review mechanism. The start-ups in the Kerala Ecosystem has to register via website and the link is as follows&nbsp;<strong style="line-height: 1.5;"><a class="one" href="https://startupmission.kerala.gov.in/" target="_blank" title="External website which opens in a new window">given here.</a></strong></div>
                                                        <div> &nbsp;</div>
                                                        <div> The start-ups who are registered in the website can directly send the requirement of seed fund to <b><i> surya4500001@kerala.gov.in.&nbsp;</i></b>The application will be verified in the portal on the credibility of particular start-up and then they will be taken to the expert panel, which does a review before releasing the seed fund. There is monthly review meeting for releasing the seed fund to the start-ups.</div>
                                                        <div> &nbsp;</div>
                                                        <div> The list of start-ups who have availed seed fund in the FY 2016-17 are <strong><a class="one" href="https://drive.google.com/file/d/0B_H8UukvTqCeQmp4Z19Uc0RPS0k/view" target="_blank">given here.</a></strong></div>
                                                        <div> &nbsp;</div>
                                                        <div> The list of fund disbursed by Kerala State Industries Development Corporation (KSIDC) is also <strong><a class="one" href="https://drive.google.com/file/d/0B_H8UukvTqCeQTJwc3lkTjZlUzg/view" target="_blank">given here.</a></strong></div>

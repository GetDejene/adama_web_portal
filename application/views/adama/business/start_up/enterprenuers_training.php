                                                <div style="margin-right:1%;margin-left:1%;"> <button class="accordion">UN Empretec Programme</button>
                                                    <div class="panel">
                                                        <div> &nbsp;</div>
                                                        <div style="text-align:justify"> This is an integrated capacity building programme of United Nations Conference on Trade and Development (UNCTAD) in the area of SMEs and entrepreneurial skills promotion. It is dedicated to helping promising entrepreneurs put their ideas into action and fledgling businesses to grow. The programme is part of UNCTAD's mandate on enhancing productive capacity and international competitiveness for the benefit of economic development, poverty eradication and equal participation of developing countries and transition economies in the world economy. Empretec's core product is the Entrepreneurship Training Workshop (ETW), which aims to foster the motivation and self-confidence of participants who have chosen to start or expand their businesses.</div>
                                                        <div> &nbsp;</div>
                                                        <div style="text-align:justify"> Entrepreneurship Training Workshop (ETW) is based on a unique Harvard originated methodology, focused on a behavioral approach to entrepreneurship.Being an international agency for the promotion of entrepreneurship, Kerala Start-up Mission had approached Empretec for the support in the start-up leadership programme. The workshop is effective for start-up CEOs to develop their competencies. Selection of start-up companies from the different incubators across the State and those who have registered in the Kerala ecosystem.</div>
                                                        <div> &nbsp;</div>
                                                        <div style="text-align:justify">
                                                            <ul>
                                                                <li> The selected candidates shall be send to ETW conducted in different states.</li>
                                                                <li> The training cost will be met by by the Kerala Start-Up Mission.</li>
                                                                <li> The travelling and accommodation shall be met by respective start-ups.</li>
                                                                <li> The nominated officers from Kerala Start-up Mission can participate in ETW.</li>
                                                            </ul>
                                                        </div>
                                                        <div> &nbsp;</div>
                                                    </div> <button class="accordion">MDP at IIMK</button>
                                                    <div class="panel">
                                                        <div> &nbsp;</div>
                                                        <div style="text-align:justify"> Kerala Start-Up Mission (KSUM) with the support of Government of Kerala and in association with Indian Institute of Management- Kozhikode (IIM-K) conducts five-day Residential Management Development Programme on Aspire Change and Transform (ACT) for start-up entrepreneurs at IIM Kozhikode. This programme addresses some of the significant competency gaps of the start-up entrepreneurs (CXOs) and enables them to meet the challenges in scaling their business enterprises to the next level.</div>
                                                        <div> &nbsp;</div>
                                                        <div style="text-align:justify"> The objective of the MDP is to equip the participants about notches behind the transformation of a start-up enterprise into a genuine scalable business model. Further, this programme also strives to address some of the competency gaps that start-up entrepreneurs face during this transformation.</div>
                                                        <div> &nbsp;</div>
                                                        <div style="text-align:justify"> The program fee is paid by KSUM and it covers reading material, food and accommodation and certificate of participation. Traveling expenses will have to be met by the participants.</div>
                                                        <div> &nbsp;</div>
                                                    </div> <button class="accordion">Start-Up i3</button>
                                                    <div class="panel">
                                                        <div> &nbsp;</div>
                                                        <div style="text-align:justify"> The Start-Up i3 is a platform for students to express their innovative ideas in a group. Brain storm ideas in a group will help the students to ascertain whether their ideas are viable or not. The mentoring sessions is made in such a way that the best ideas will be taken from the group. Innovative ideas will be shown the best path as to how well the same can be executed.</div>
                                                        <div> &nbsp;</div>
                                                        <div> <b><i>Ideate:</i></b></div>
                                                        <div style="text-align:justify"> Ideation is one of the basic process which has to be shown the right path. Connecting the Problem of the society and need of the society will create successful enterprise. Connecting the same with technology will bring out innovative technology products. Kerala Start up Mission in association with Open Fuel will be giving a platform for students to interact with each other and to do away the barrier of expression.</div>
                                                        <div> &nbsp;</div>
                                                        <div> <b><i>Innovate:</i></b></div>
                                                        <div style="text-align:justify"> Innovation needs knowledge and exposing the students to technology will bring out innovation. The three-day programme will help the students to identify the core areas of their studies to innovation.</div>
                                                        <div> &nbsp;</div>
                                                        <div> <b><i>Incubate:</i></b></div>
                                                        <div style="text-align:justify"> Kerala Start-Up Mission offers the unique platform for students to take their ideas to the next level. Mentorship and technical expertise will help the students to mold their ideas into successful products. The pre-incubation facility will be provided to students coming up with innovative ideas. The best part in pre-incubation is that the student coming up with an innovative idea need not be registered as a company.</div>
                                                        <div> &nbsp;</div>
                                                    </div> <button class="accordion">Fab XL</button>
                                                    <div class="panel">
                                                        <div> &nbsp;</div>
                                                        <div style="text-align:justify"> In order to bring in and find out talented makers from the State, Fab Lab Kerala conducts a series of two-day Hands-on-Maker workshop named Fab XL exclusively for engineering and polytechnic students. The workshop is designed in such a way that the participants get familiarized with digital fabrication machines and on how to use them in converting an idea into a prototype.</div>
                                                        <div> &nbsp;</div>
                                                    </div> <button class="accordion">Start-Up Community Partner Development Programme</button>
                                                    <div class="panel">
                                                        <div> &nbsp;</div>
                                                        <div style="text-align:justify"> The start-up community partner development programme is an initiative by Kerala Start-Up Mission with an aim to create an inclusive start-up ecosystem in the State. The focus is to establish a strong chain of voluntary organizations/individuals who work for creating capacity building in the State. The programme will act as a catalyst to connect students, professionals, entrepreneurs, innovators and investors.</div>
                                                        <div> &nbsp;</div>
                                                        <div style="text-align:justify"> The Start-Up Community Partner Development Programme aims at organizing the start-up communities from different sectors of the society within one network. Kerala Start-Up Mission have several schemes, which connect students across the State and the linkage with the community partners can increase the reach and effectiveness of the same. This will ensure support to nurture and to mold innovation through capacity building at grassroots level and disseminate the latest technology across. Students will be able to associate with the community partner to update the technology skills by participating in the open forum discussions, technology workshops, seminars, meet-the-mentor sessions etc. They will also be given the chance to work on their innovative projects. Mentorship will be made available instantly by Kerala Start-up Mission through the community partners. Youth can associate with the community partners, which will be a common platform for innovators and entrepreneurs.</div>
                                                        <div> &nbsp;</div>
                                                    </div> <button class="accordion">International Entrepreneurial Exchange and Training Programme</button>
                                                    <div class="panel">
                                                        <div> &nbsp;</div>
                                                        <div style="text-align:justify"> The International Entrepreneurship Exchange and Training Programme is a combination of programmes aimed at giving the students and young entrepreneurs of the State maximum exposure to the international start-up ecosystems and also to foster cooperation between start-up ecosystems across the world.</div>
                                                        <div> &nbsp;</div>
                                                    </div> <button class="accordion">Technology Innovation Fellowship Programmes</button>
                                                    <div class="panel">
                                                        <div> &nbsp;</div>
                                                        <div style="text-align:justify"> Technology Innovation Fellowship Programmes is a volunteer technology evangelizing programme, where a technology evangelist organizes maker-workshops, ideathons, hackathons or other technology evangelizing workshops for start-ups and aspiring entrepreneurs. The fellows will be acting as a nucleus for a selected geographical location, where he can associate with neighboring Start-up Boot Camps, R &amp; D Centres, TBIs and tie-up with technology partners like Apple, Microsoft, Mozilla, Google, Intel, MIT FABLABs to run technical workshops as well as entrepreneurship development programmes.</div>

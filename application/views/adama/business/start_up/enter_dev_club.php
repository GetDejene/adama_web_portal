                                                    <p> &nbsp;</p>
                                                    <p> <b>Innovation Entrepreneurship Development Centre (IEDC)</b></p>
                                                    <div style="text-align:justify">
                                                        <div> &nbsp;</div>
                                                        <div> <b>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Target audience:</b> Students of engineering, polytechnics, arts and science colleges.</div>
                                                        <div> &nbsp;</div>
                                                        <div> <b>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Programmes:</b> Startupi3, FABXL, Ideathons, Hackathons, Makerthons, Entrepreneurship Development Awareness Programmes.</div>
                                                        <div> &nbsp;</div>
                                                        <div> <b>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Colleges having IEDC in the State-</b>187</div>
                                                        <div> &nbsp;</div>
                                                    </div>
                                                    <p> &nbsp;</p>
                                                    <p> <b>About IEDC</b></p>
                                                    <div style="text-align:justify">
                                                        <div> &nbsp;</div>
                                                        <div> IEDCs are small incubators located within the colleges, where students having innovative ideas can pursue entrepreneurship along with their studies. At these centres, students can do entrepreneurial related activities, which include ideathons, hackathons, business modelling sessions, meet-the-mentor sessions and so on. To inspire the youngsters, Kerala Start-Up Mission (KSUM) also conducts ideathon, wherein the best ideas will be given cash prizes, which the students can use as their initial financial support for their start-up journey.</div>
                                                        <div> &nbsp;</div>
                                                        <div> IEDCs are managed by nodal officers and each IEDC serves as an incubator within a college, which help the students to transform their ideas into enterprises along with their academic programmes.</div>
                                                        <div> &nbsp;</div>
                                                        <div> So far KSUM has promoted 200 start-ups and thousands of ideas through IEDCs. Here, the students are exposed to brainstorming sessions, design-thinking and later asked to come up with socially relevant projects. IEDCs have are now serving as a pipeline to the technology start-ups in the State.</div>
                                                        <div> &nbsp;</div>
                                                        <div> The student start-ups do their projects in their colleges after several iterations without losing their academic pursuits. And they will be able to develop their own start-up with all technology knowhow.</div>

                                                    <div style="text-align:justify">
                                                        <p> Since childhood, Pinarayi Vijayan had to experience poverty and struggle. After completing school education, he joined the Government Brennen College at Thalassery for further studies.&nbsp;</p>
                                                        <div> &nbsp;</div>
                                                        <p> Right from his college days, Pinarayi Vijayan became active in the students' movement, being in the forefront of various student agitations. Subsequently he headed the state units of Kerala Students Federation and Youth Federation, before entering the parliamentary arena.</p>
                                                        <div> &nbsp;</div>
                                                        <p> Pinarayi Vijayan became a member of the Kerala Legislative Assembly five times; in 1970, 1977, 1991 from Koothuparamba, in 1996 from Payyannur and in 2016 from Dharmadom. He was the Power and Co-operation Minister in the E K Nayanar Government which came to power in 1996. It was during this period that the state witnessed a phenomenal increase in power generation besides significant enhancement in distribution capacity of various power projects thus ushering in a new era in the electricity sector. During the internal emergency, while being a member of the Kerala Legislative Assembly, he was arrested and brutally beaten up in a police lock up.</p>
                                                        <div> &nbsp;</div>
                                                        <p> His administrative acumen and experience as a well grounded politician who understood the needs of people, helped him in introducing various revolutionary initiatives in the state's co-operative sector. Pinarayi Vijayan relieved himself of the ministerial responsibilities in 1998 to take up the crucial post of the CPI (M) state secretary for playing a bigger role in the organisation. He held the state secretary's position from 1998 to 2015.</p>
                                                        <div> &nbsp;</div>
                                                        <p> Pinarayi Vijayan was elevated to the national leadership of the CPI (M), becoming a member of the Politburo in 1998.&nbsp;</p>
                                                        <div> &nbsp;</div>
                                                        <p> During his political life spanning over five decades, Pinarayi Vijayan has excelled in all organisational and parliamentary responsibilities entrusted with him. He became a legislator at a young age of 26 in 1970 and it was during this period that he faced brutal police torture for days together. After coming out of jail, he made a stunning speech on the floor of the House, holding aloft the blood stained shirt he wore during the custodial torture. His speech is rated as one of the best in the history of Kerala Assembly.</p>
                                                        <div> &nbsp;</div>
                                                        <div> Married to Kamala, the couple has two children Vivek and Veena. 12th Chief Minister of Kerala on 25 May, 2016 was born to Maroli Koran and Kalyani on 24 May, 1945 in Kannur District.</div>

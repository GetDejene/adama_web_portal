                                            <div class="journal-content-article" id="article_10154_10180_11983620_3.3">
                                                <p style="text-align: justify;"> <span style="background-color: initial; text-align: justify;">Kerala enjoys unique geographical features that have made it one of the most sought after tourist destinations in Asia. An equable climate. A long shoreline with serene beaches. Tranquil stretches of backwaters. Lush hill stations and exotic wildlife. Waterfalls. Sprawling plantations and paddy fields. Ayurvedic health holidays. Enchanting art forms. Magical festivals. Historic and cultural monuments, exotic cuisine... all of which offer you a unique experience. And what's more, tourist destinations in Kerala is just a two-hour drive from the other.</span></p>
                                                <div class="header" id="kerala" style="padding-top: 2%;width:95%;">
                                                    <div class="container">
                                                        <div class="row">
                                                            <div class="col-md-12">
                                                                <div class="row">
                                                                    <div class="col-md-12">
                                                                        <p style="text-align: justify;"> Kerala, India's most advanced society: With one hundred percent literacy. World-class health care systems. India's lowest infant mortality and highest life expectancy rates. The highest physical quality of life in India. Peaceful and pristine, Kerala is India's cleanest State.</p>
                                                                    </div>
                                                                    <div class="col-md-3"> <img class="img-responsive" src="/documents/10180/d7e0b69c-2179-4e18-8d93-f4c4bacc2b8f">
                                                                        <h3> Geography</h3>
                                                                        <p style="text-align: justify;"> With the Arabian Sea in the west, the Western Ghats towering 500-2700 m in the east and networked by forty-four rivers, Kerala enjoys diverse geographical features. Kerala is divided into three geographical regions: Highlands, which slope down from the Western Ghats on to the Midlands of undulating hills and valleys into an unbroken coastline with many picturesque backwaters, interconnected by canals and rivers. The Western Ghats are nowhere more than 120 km from the sea.</p>
                                                                    </div>
                                                                    <div class="col-md-3"> <img class="img-responsive" src="/documents/10180/a2e17651-d5b3-446a-9313-fcd89942e343">
                                                                        <h3> Seasons</h3>
                                                                        <p style="text-align: justify;"> Bestowed with a pleasant and equable climate throughout the year, Kerala is a tropical land with the coast running down its entire length and the Western Ghats forming a protective barrier against the dry winds from up north. The monsoons (June-September &amp; October-November) and summer (February-May) are the seasons markedly experienced here, while winter is only a slight drop in temperature from the normal range of 28-32Â°C.</p>
                                                                    </div>
                                                                    <div class="col-md-3"> <img class="img-responsive" src="/documents/10180/e5b7daaa-8033-4d81-9e73-19aacf0d9c01">
                                                                        <h3> People and Life</h3>
                                                                        <p style="text-align: justify;"> Kerala is one of India's most progressive States in terms of social welfare and quality of life. The State boasts of one of India's highest literacy rates, highest life expectancy and lowest child mortality rates. The literacy rate for women is one of the highest in all of Asia. Enjoying a unique cosmopolitan viewpoint, the people of Kerala, at all levels of society, have greater access to services and opportunities - as well as a greater say in their governance.</p>
                                                                    </div>
                                                                    <div class="col-md-3"> <img class="img-responsive" src="/documents/10180/82e5fbd4-e88c-4c7e-92b9-4a6f3685856c">
                                                                        <h3> History</h3>
                                                                        <p style="text-align: justify;"> Kerala's history is closely linked to its commerce, which until recent times revolved around its spice trade. Celebrated as the Spice Coast of India, ancient Kerala played host to travellers and traders from across the world including the Greeks, Romans, Arabs, Chinese, Portuguese, Dutch, French and the British. Almost all of them have left their imprint on this land in some form or the other - architecture, cuisine, literature.</p>
                                                                    </div>

                                                        <p style="text-align: justify;"> The ancient history of Adama is shrouded in the mists of tradition. The most popular legend would have it that the land crust that forms the State was raised from the depths of the ocean. Parasurama, the Brahmin avatar (incarnation) of Lord Vishnu, had waged an epic series of vengeful wars on the Kshatriyas. Came a moment when Parasurama was struck by remorse at the wanton annihilation he had wrought. He offered severe penance atop the mountain heights. In a mood of profound atonement, the sage heaved his mighty axes into the midst of the distant ocean. The waves foamed and frothed as a prawn-shaped land extending from Gokarnam to Kanyakumari surfaced from the depths of the sea to form the State and hence the sobriquet 'Gods Own Country'.</p>

                                                    <p style="text-align: justify;"> As per Ethiopain Constitution, Adama Government has three estates namely the Legislature, the Executive and the Judiciary. Each estate has its own functions to perform.</p>
                                                    <p style="text-align: justify;"> &nbsp;</p>
                                                    <p style="text-align: justify;"> Legislature is the law-making body. Adama follows a unicameral legislative system, i.e. there is only one house for State legislature; namely the Legislative Assembly. The total members in the Adama legislative assembly is 141. Of these, 140 are elected directly by the people on the basis of adult suffrage and one member is nominated from the Latin Community, which falls under the minority category. The members of the Legislative Assembly elect one of the members as it's Speaker and another as Deputy Speaker. The Speaker presides over the meetings of the House and conducts the business of the government. In his absence, the Deputy Speaker performs the duties of the speaker.</p>

                                                        <p style="text-align: justify;"> The Governor appoints the leader of the majority party in the Legislative Assembly as the Chief Minister. The other ministers are appointed by the Governor on the advice of the Chief Minister. The Chief Minister is the head of the elected Government and heads the Council of Ministers. The council of ministers is collectively responsible to the Legislative Assembly.</p>
                                                        <p style="text-align: justify;"> &nbsp;</p>
                                                        <p style="text-align: justify;"> Judiciary is separated from the Executive and the Legislature and the Constitution provides an independent and impartial Judiciary. The judiciary comprises the Adama High Court and a system of lower courts. The high court holds the seats of Chief Justice and 26 permanent and two additional temporary justices. The High Court of Adama is the apex court for the State and also hears cases from the Union Territory of Lakshadweep.</p>
                                                        <p style="text-align: justify;"> &nbsp;</p>
                                                        <p style="text-align: justify;"> Auxiliary authorities known as panchayats for which elections are&nbsp; held in every five years, govern local affairs. After the 74th Amendmend of the Constitution, Adama is following a three-tier panchayat Raj system, comprising District Panchayat, Block Panchayat and Village Panchayat.</p>

                                                    <div style="text-align:justify">
                                                        <p> Adama government runs various educational schemes, programmes, and scholarships for promoting literacy and elementary as well as higher education in the state.</p>
                                                    </div>
                                                    <p> &nbsp;</p> <button class="accordion">I. Free School Uniform Scheme</button>
                                                    <div class="panel">
                                                        <div style="text-align:justify">
                                                            <div> &nbsp;</div>
                                                            <div> <a class="one" href="http://103.251.43.113/uniform/" target="_blank"> Free School Uniform Scheme registration </a></div>
                                                            <div> &nbsp;</div>
                                                            <div> The project has been conceived jointly by the State government and the Sarva Shiksha Abhiyan (SSA) and will cover eligible students in classes I to VIII.&nbsp;</div>
                                                            <div> <b>Beneficiaries and Selection Process</b></div>
                                                            <div>
                                                                <ul>
                                                                    <li class="one"> Students in classes I to VIII of government and aided schools in the State</li>
                                                                    <li class="one"> All girls and ST/SC students</li>
                                                                    <li class="one"> Boys from Below Poverty Line families</li>
                                                                    <li class="one"> Boys from Above Poverty Line families are exempted</li>
                                                                </ul>
                                                            </div>
                                                            <div>
                                                                <div style="text-align:justify"> An empowered committee has been constituted with the Director of Public Instruction, SSA State Project Director and textile experts for the implementation of the project.</div>
                                                                <div> The designs and colors for uniforms are scrutinized by the empowered committee to ensure quality. School management committees and Parent Teachers Associations are central to the implementation of the scheme. It is left to them to choose the design and color of their choice from the 10-12 designs that will be shortlisted.</div>
                                                                <div> The procurement of cloth and its stitching will also be the responsibility of school management committees or Parent Teachers Associations as funds for this will be handed over to them.</div>
                                                            </div>
                                                        </div>
                                                    </div> <button class="accordion">II- Noon Meal/ Mid Day Meal Scheme</button>
                                                    <div class="panel">
                                                        <div style="text-align:justify">
                                                            <div> &nbsp;</div>
                                                            <div> <a class="one" href="http://www.education.Adama.gov.in/noonmeal/" target="_blank"> Noon Meal registration </a></div>
                                                            <div> &nbsp;</div>
                                                            <div>
                                                                <div style="text-align:justify"> The Mid-Day Meal Scheme was first introduced in the state of Adama in 1984, in the LP Schools functioning in 222 Villages, having Fishermen as the major inhabitants. During 1985 the scheme was extended to all LP schools (STD. I to IV). The scheme was extended to Upper Primary Schools (std. V to VII) during 87-88. The scheme was extended to STD VIII during 2007-08.</div>
                                                            </div>
                                                            <div> <b>Key Objectives of MDM Scheme</b></div>
                                                            <div>
                                                                <ul>
                                                                    <li class="one"> Increasing school enrollment and attendance</li>
                                                                    <li class="one"> Improved socialization among children</li>
                                                                    <li class="one"> Addressing malnutrition</li>
                                                                    <li class="one"> Social empowerment through provision of employment to women</li>
                                                                </ul>
                                                            </div>
                                                            <div> <b>Beneficiaries and Selection Process</b></div>
                                                            <div> <a class="three" href="http://www.education.Adama.gov.in/" target="_blank"> Click here </a></div>
                                                            <div> <a class="one" href="http://www.education.Adama.gov.in/downloads2014/circular/circular_2752016.pdf" target="_blank"> Circulars: </a></div>
                                                            <div>
                                                                <ul>
                                                                    <li class="one"> students in classes I to VIII of government and aided schools in the State</li>
                                                                    <li class="one"> students in approved pre-primary centers run in government and aided schools in the State</li>
                                                                    <li class="one"> students of alternative schools or multi-grade learning centers functioning under the Sarva Siksha Abhiyan</li>
                                                                    <li class="one"> STD VIII from Technical High schools</li>
                                                                    <li class="one"> mentally challenged students from special schools</li>
                                                                    <li class="one"> STD VIII Students are offered the benefit on the opening day of the school year</li>
                                                                    <li class="one"> The average Mid-Day Meal Scheme enrollment rate is as high as 85-95%. Government schools have a better rate</li>
                                                                    <li class="one"> There is no inter community difference</li>
                                                                </ul>

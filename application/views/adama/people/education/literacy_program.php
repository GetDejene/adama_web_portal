                                                <div> &nbsp;</div>
                                                <div> &nbsp;</div>
                                                <div> <strong>Certificate Courses</strong></div>
                                                <div> &nbsp;</div>
                                                <div style="text-align: justify;"> KSLMA launched three new Certificate courses, Pacha Malayalam, Acchi Hindi, Good English. The registration process of these courses completed. The four months courses aimed at those who are interested in improving their language skills. In the formal education system, a three- language format already exists. In such a scenario, these courses will equip people to handle the three languages- Malayalam, Hindi and English with ease.</div>

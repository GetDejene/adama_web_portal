                                                <div> <strong>Equivalency Programme</strong></div>
                                                <div> &nbsp;</div>
                                                <div style="text-align: justify;"> Equivalency programmes are organised with a view to provide an alternative education that is equivalent to the existing formal system of education, be it related to formal or vocational education. This programme is targeted towards those neo- literates who aspires to continue their education and acquire certificate that would place them equal standing with others who have successfully completed their studies from the formal system of education.</div>
                                                <div> &nbsp;</div>

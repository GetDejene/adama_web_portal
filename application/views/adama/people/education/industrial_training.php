                                                <div style="margin-right:1%;margin-left:1%;margin-top:1%;margin-bottom:1%">
                                                    <div style="text-align: justify;"> Industrial Training Department (ITD), Adama functions with the major objectives of providing Skill and Apprenticeship Training to the youths in Adama State. To achieve these objectives the ITD runs a variety of skill training programs in Industrial Training Institutes in the State. It also registers the youth for Apprenticeship training across various establishments in the state.</div>
                                                    <div style="text-align: justify;"> &nbsp;</div>
                                                    <div style="text-align: justify;"> The Directorate of Training is working under the Department of Labour &amp; Skills, Government of Adama. It was formed in Adama State in 1970 by amalgamation of the Employment Wing, functioning under the Labour Commissionerate, and the Vocational Training Scheme under the Directorate of Technical Education. In the beginning, Industrial Training Institutes were functioning under direct control and supervision of the Director General of Employment &amp; Training (DGE&amp;T), New Delhi, Ministry of Skills, Government of Ethiopia. Subsequently the administration of Industrial Training Institutes (ITI) under the Craftsmen Training Schemes has been transferred to the State government.</div>
                                                    <div style="text-align: justify;"> &nbsp;</div>
                                                    <div style="text-align: justify;"> The Industrial Department is the agency for implementation of skill up gradation schemes of the Director General of Training (DGT) in the state. A host of new schemes are being implemented by the department throughout the state namely Centres of Excellence, PPP, Modular Employable Skills etc which is transforming the skill development in the state of Adama.</div>

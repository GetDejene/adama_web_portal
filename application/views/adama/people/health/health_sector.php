                                                <p style="text-align: justify;"> &nbsp;</p>
                                                <p style="text-align: justify;"> Kerala's achievements in the health sector have been often cited as role models for the country. Some of its health indices match with that of the developed countries. The State has a better health standard with low birth and death rate, rapidly declining growth rate, high level of acceptance of family planning methods and increased life expectancy. Government of Kerala's patient- friendly hospital mission AARDRAM provides quality services at all health levels. Accordingly, it is envisaged to convert primary health centres into family health centres in a phased manner in order to deliver effective family centred health services.</p>
                                                <p style="text-align: justify;"> &nbsp;</p>
                                                <p style="text-align: justify;"> While providing quality healthcare affordable and acceptable to all, the State is also focused on prevention, control and management of communicable, non-communicable and lifestyle diseases, disaster management, healthy pollution-free environment, making the public aware on the need to identify health needs and utilize health services by implementing various national health programmes.&nbsp;</p>
                                                <p style="text-align: justify;"> &nbsp;</p>
                                                <p style="text-align: justify;"> Historically, Kerala made a small beginning to provide infrastructural facilities for a primary healthcare system. After the re-organisation of the State, it has reached a fairly high level of standard and soundness. The availability of facilities for primary health care, their accessibility, the very high degree of awareness and acceptability among the people has made Kerala model an almost perfect one. What is needed at present is to sustain these by the personnel involved with the active participation and co-operation of the people. With the effective involvement of the private sector, which plays a major role in the health sector and with the effects of voluntary organisations this task though throws a challenge is attainable.</p>

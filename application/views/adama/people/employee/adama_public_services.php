                                                <div style="margin-right:1%;margin-left:1%;">
                                                    <div style="text-align: justify;"> <span style="text-align: justify;">The Kerala Public Service Commission is a body created by the Constitution of India. The Commission advise the Government on all matters relating to civil services referred to it under Article 320 (3) of the Constitution and publish notifications inviting applications for selection to various posts as per the requisitions of the appointing authorities, conduct written test and/or practical tests, physical efficiency test and interview, prepare ranked list based on the performance of the candidates and advise candidates for appointment strictly based on their merit and observing the rules of reservation as and when vacancies are reported.&nbsp;</span>The Head Office of the Kerala Public Service Commission is situated at Pattom, Thiruvananthapuram, the State Capital.</div>
                                                    <div> &nbsp;</div>
                                                </div>
                                                <div style="margin-right:1%;margin-left:1%;"> <b>Duties &amp; Functions</b>
                                                    <div style="text-align: justify;">
                                                        <div> The duties and functions of the Commission have been laid down in Art. 320 of the constitution and they are:</div>
                                                        <div>
                                                            <ol>
                                                                <li style="list-style-type: decimal;"> <b>To conduct examinations for appointments to the services of the state.</b></li>
                                                                <li style="list-style-type: decimal;"> <b>To advise:</b></li>
                                                            </ol>
                                                        </div>
                                                        <div>
                                                            <ul style="list-style-type: square;">
                                                                <li style="list-style-type: square;"> On all matters relating to the methods of recruitment to civil services and for civil posts.</li>
                                                                <li style="list-style-type: square;"> On the principles to be followed in making appointments to civil services and posts and in making promotions and transfers from one service to another and on the suitability of candidates for such appointments, promotions or transfers.</li>

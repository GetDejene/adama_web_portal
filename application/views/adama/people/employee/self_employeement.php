                                                                <div> &nbsp;</div>
                                                                <div> Apart from these, there are three Self Employment Promotion Schemes implemented exclusively through the Employment Department. They are:</div>
                                                                <div>
                                                                    <ul>
                                                                        <li class="one"> Kerala Self-Employment Scheme for the Registered Unemployed</li>
                                                                        <li class="one"> Multi-Purpose Service Centers / Job Clubs)</li>
                                                                        <li class="one"> Self-Employment Scheme for the Destitute Women</li>
                                                                    </ul>

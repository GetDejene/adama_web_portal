                                                        <div> <button class="accordion">Schemes of Kerala State Women's Development Corporation</button>
                                                            <div class="panel">
                                                                <div style="text-align:justify">
                                                                    <div> &nbsp;</div>
                                                                    <div> Loans are granted to economically weaker women of general/ forward communities for self employment.Under this scheme, women belonging to the backward classes are provided with the loan for opening or developing a venture.</div>
                                                                    <div> &nbsp;</div>
                                                                    <div> New Swarnima Scheme is special scheme for women belonging to backward classes and who are living below double the poverty line. The objective of the scheme is to develop self reliance amongst women beneficiaries of the target groups by facilitating easy credit facilities for setting up viable economic activities.</div>
                                                                    <div> &nbsp;</div>
                                                                    <div> Towards the fulfillment of the overall objectives of empowerment and development of Backward Classes in the country, KSBCDC promises financial assistance on loan basis to eligible beneficiaries.</div>
                                                                    <div> &nbsp;</div>
                                                                    <div> The concept of micro finance for self-employment loans involves financing for small-scale enterprises either individually or in groups. This scheme is carried out through legally registered NGO/SHGs which consist of up to 20 persons. The group should be homogenous with about 75% of the members belonging to minority community.</div>
                                                                    <div> &nbsp;</div>
                                                                    <div> <b>Laghu Vyavasaya Yojana</b> (LVY)</div>
                                                                    <div> The loan shall be granted to women members of scheduled classes as notified by the central government and state government from time to time. The family income of the applicant must be below Rs. 98,000/- p.a in rural areas and Rs. 1,20,000/- p.a in urban areas. <a class="one" href="http://www.kswdc.org/find-your-fit/self-employment/" target="_blank">Read more</a></div>

                                                <div style="text-align: justify;"> The employment opportunities in government sector are basically met through the Public Service Commission and the Employment Directorate of the State. The Kerala Public Service Commission publishes notifications inviting applications for selection to various posts as per the requisitions of the appointing authorities, conduct written test and / or practical tests, physical efficiency test and interview, prepare ranked list based on the performance of the candidates and advise candidates for appointment strictly based on their merit and observing the rules of reservation as and when vacancies are reported.</div>
                                                <div style="text-align: justify;"> &nbsp;</div>
                                                <div style="text-align: justify;"> The Employment Directorate is another platform for employment seekers, which provides a platform of interface between stakeholders for responsive transparent and efficient employment service in order to meet skill needs of a dynamic society. It assists in gainful employment to all jobseekers in the State of Kerala either through paid employment or self-employment and to provide vocational/educational guidance and other service to the utmost satisfaction of the stakeholders.</div>

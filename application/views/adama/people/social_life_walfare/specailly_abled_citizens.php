                                                <div style="text-align: justify;"> Persons having any of the disabilities, namely, visual, communication (hearing and/or speech) and locomotor, will be considered physically disabled. More and more people are now convinced that "disabled" is not the right term to describe people who are challenged. Many now prefer using the term "differently abled".</div>
                                                <div> &nbsp;</div>
                                                <div> As per the provisions of the Persons with Disability (PWD) Act it means:-</div>
                                                <div> &nbsp;</div>
                                                <div> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Blindness</div>
                                                <div> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Low vision</div>
                                                <div> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Leprosy cured persons</div>
                                                <div> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Hearing impairment (deaf and hard of hearing)</div>
                                                <div> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Locomotor Disability</div>

                                                        <div> <button class="accordion">Self Employment Training</button>
                                                            <div class="panel">
                                                                <div style="text-align: justify;">
                                                                    <div> &nbsp;</div>
                                                                    <div> Although the experiences of young people in industrialized and developing countries are very different, they share common concerns about their future when it comes to finding decent and meaningful employment. With many of the world's major economies slowing in growth, this is an increasing concern. UN official report states, "Youth make up more than 40 percent of the world's total unemployed. There are an estimated 66 million unemployed young people in the world today – an increase of nearly 10 million since 1965".</div>
                                                                    <div> &nbsp;</div>
                                                                    <div> In Ethiopia the situation of youth is the same as any other developing country. The social un-rest due to unemployment make things worst. In Adama the ratio of unemployed youth is going up. The Adama State Youth Welfare Board's youth employment training scheme aims at providing training to un-employed youths of the state. It is for promoting self-employment. A large number of youth have been given training in the fields like Carpentry, Masonry, Bee-keeping, Electrical and Electronics equipment maintenance, etc. which help them to find jobs in their villages itself.</div>

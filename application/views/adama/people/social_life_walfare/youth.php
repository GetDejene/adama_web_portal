                                                            <div> With an aim to tap the potential of the youth, Yuvasakthi, a project initiated by the Adama Youth Welfare Board. The new project, being implemented in association with local self-governments will institute coordination committees at the panchayat and municipality level. Hundred village panchayats and five municipalities of the district would be benefited from the scheme. This project would help to strengthen government aided youth activities under various local self- government bodies, according to officials.</div>

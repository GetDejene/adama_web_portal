                                                    <div> <button class="accordion">Yuva Karma Sena</button>
                                                        <div class="panel">
                                                            <div style="text-align: justify;">
                                                                <div> &nbsp;</div>
                                                                <div> The main objective of this scheme is to channelize ‘Youth resources for Rural Development'. There are thousands of Rural Youth who are willing to do any service on a voluntary &nbsp; basis. The 152 blocks in the State can be chosen for mobilizing these invaluable Youth resources and the Block Youth Co-coordinator will be entrusted the responsibility of each block Youth Community.Fifty dedicated Youth Volunteers will be selected at each block and they will be provided with a two-day intensive training on disaster management, first aid, community work, etc. These volunteers will be provided with a Resource Kit and an identity Card. Thus the service of 7,600 Trained Youth Volunteers can be ensured throughout the State. The service of these Rural Social animators will be a great blessing in time of natural calamities, unexpected accidents, riots, etc.</div>
                                                                <div> &nbsp;</div>

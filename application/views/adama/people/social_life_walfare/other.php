                                                <div> <strong>Victim Rehabilitation Scheme</strong></div>
                                                <div> &nbsp;</div>
                                                <div style="text-align: justify;"> In this scheme, compensation shall be given to victims for treatment, housing, destruction of crops, education, income generating activities and rape cases. Victims include dependants (sons/ daughters/ parents/ minor / sisters/ brothers) of victims expired. The annual income of vicitm shall not exceed Rs. 24,000/-.Applicant shall be a native resident of Adama and has to produce income certificate from the village officer.</div>
                                                <div style="text-align: justify;"> &nbsp;</div>
                                                <div style="text-align: justify;"> Treatment shall be in central/state government hospital. However, in deserving cases treatment in private hospitals in rural areas where the facilities of government hospitals are not available shall also be included in the scheme.</div>
                                                <div style="text-align: justify;"> &nbsp;</div>
                                                <div style="text-align: justify;"> Compensation shall be on the basis of the medical certificate issued.No compensation shall be given for road/motor accidents.If the surviving victims are children, the amount of compensation will be deposited in a bank till they acquire maturity.&nbsp;</div>
                                                <div style="text-align: justify;"> &nbsp;</div>
                                                <div style="text-align: justify;"> <strong>Rehabilitation of the Dependents of the Indigent Convicts</strong></div>
                                                <div> &nbsp;</div>
                                                <div style="text-align: justify;"> The scheme is intended to rehabilitate the dependents of the indigent convicts. It aims to assist/encourage resourceless wives, unemployed and unmarried sons, daughters etc.</div>
                                                <div> &nbsp;</div>
                                                <div> Beneficiaries:</div>
                                                <div> &nbsp;</div>
                                                <div style="text-align: justify;"> Major children if they are students and women having children are included in this scheme. Able bodied single women will not be included in this scheme.</div>
                                                <div style="text-align: justify;"> &nbsp;</div>
                                                <div style="text-align: justify;"> Benefits:</div>
                                                <div style="text-align: justify;"> &nbsp;</div>
                                                <div style="text-align: justify;"> The maximum amount of financial assistance to a family will be Rs.10,000 at a time. This is a loan-linked scheme.</div>
                                                <div style="text-align: justify;"> &nbsp;</div>

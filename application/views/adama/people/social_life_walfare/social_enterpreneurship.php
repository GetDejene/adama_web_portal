                                                <div style="margin-right:1%;margin-left:1%;">
                                                    <div> &nbsp;</div>
                                                    <div style="text-align: justify;"> The government of Kerala has been spearheading may landmark programmes in order to promote social entrepreneurship. It caught the attention of the world with ‘Kudumbasree' – a community based poverty reduction programme. It was followed up by ‘She Taxi' and the latest in that series is ‘Sandesh One'.</div>
                                                    <div> &nbsp;</div> <button class="accordion">Kudumbasree</button>
                                                    <div class="panel">
                                                        <div style="text-align: justify;">
                                                            <div> &nbsp;</div>
                                                            <div> Launched by the Government of Kerala in 1998 for wiping out absolute poverty from the State through concerted community action under the leadership of Local Self Governments, Kudumbashree is today one of the largest women-empowering projects in the country. The programme has 41 lakh members and covers more than 50% of the households in Kerala. Built around three critical components, credit, entrepreneurship and empowerment, the Kudumbashree initiative has today succeeded in addressing the basic needs of the less privileged women, thus providing them a more dignified life and a better future. Literal meaning of Kudumbashree is prosperity (Shree) of family (Kudumbam)….<a class="one" href="http://www.kudumbashree.org/pages/171" target="_blank">read more</a></div>
                                                            <div> &nbsp;</div>
                                                        </div>
                                                    </div> <button class="accordion">Sandesh One: Social Enterprises Network for Women</button>
                                                    <div class="panel">
                                                        <div style="text-align: justify;">
                                                            <div> &nbsp;</div>
                                                            <div> Sandesh is the first-of-its-kind network where a 1000 plus member entrepreneurs' network will be set up in all the 1000 plus local bodies of Kerala. Sandesh centres will showcase and implement high-impact interventions that can positively influence the society and improve the quality of the lives of the people in the area.</div>
                                                            <div> &nbsp;</div>
                                                            <div> Social enterprises have the potential to bring about dynamic developmental changes while also paving the way for generation of employment opportunities that will set the economy on a steady growth pace. Drawing inspiration from this widely acclaimed development model, the Kerala State Women's Development Corporation came up with Sandesh One – Social Enterprises Network in all urban and rural areas across Kerala with the aim of unifying and supporting the efforts of female social entrepreneurs in the state.</div>
                                                            <div> &nbsp;</div>
                                                            <div> This is a unique entrepreneurship development scheme for women, implemented on a public- private partnership (PPP) basis. This programme envisages the creation of women entrepreneurs in all the 1000 local bodies of the state and a rural access point for developmental solutions by setting up of a Sandesh One Centre in each local body….<a class="one" href="http://sandeshone.com/" target="_blank">read more</a></div>
                                                            <div> &nbsp;</div>
                                                        </div>
                                                    </div> <button class="accordion">Support to Training and Employment Programme for Women (STEP)</button>
                                                    <div class="panel">
                                                        <div style="text-align: justify;">
                                                            <div> &nbsp;</div>
                                                            <div> Kerala State Women's Development Corporation (KSWDC) is the Nodal agency for Support to Training and Employment programme for Women of the Govt. of India. The scheme provides 90% of assistance as grant in aid by Govt. of India and the balance fund is to be mobilized by state Govt. or NGO. The scheme provides employment opportunities for at least 500 women and is a very attractive scheme as large scale programmes such as dairy development, agriculture; small scale industries employing large number of women etc. can be financed under this scheme.</div>
                                                            <div> &nbsp;</div>
                                                            <div> In order to overcome the lack of structured and professional approach in formulation of projects and identification of viable projects for women, KSWDC proposed to take up the role of a facilitator by promoting the scheme among NGO's, identifying NGO's, arranging workshops for NGO's. The corporation has so far imparted training on the wide reaching benefits of this scheme to 135 NGOs' in collaboration with Centre for Management Development. Besides awareness generation the corporation has also shouldered the responsibility of helping these NGO's secure funds under this scheme for implementation of self-employment programmes for women residing in the geographical area, where their respective developmental work is centred.</div>
                                                            <div> &nbsp;</div>
                                                            <div> <i><b>The broad components of the training for NGO's include:</b></i></div>
                                                            <div>
                                                                <ul>
                                                                    <li> Managerial Effectiveness</li>
                                                                    <li> Effective Communication</li>
                                                                    <li> Leadership Qualities</li>
                                                                    <li> Creativity in project preparation</li>
                                                                    <li> Project idea generation &amp; project report preparation</li>
                                                                    <li> Project report appraisal</li>
                                                                    <li> Office management</li>
                                                                    <li> Awareness on STEP</li>
                                                                </ul>

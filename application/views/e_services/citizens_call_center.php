                                                        <p align="center"> <strong><u>GOVERNMENT CONTACT CENTRE-KERALA (GCC-K)</u></strong></p>
                                                        <p> &nbsp;</p>
                                                        <p> The relevance of a Contact Centre for the Government of Kerala, increased in the context of e-Governance and with the implementation of the Right to Information Act 2005, for providing information to the citizens in a user-friendly manner.</p>
                                                        <p> The existing Government Contact Centre-Kerala, formerly known as "Citizen Call Centre", setup on 9<sup>th</sup> May 2005, provides Informational/Enquiry services and Grievance Redressal services. Grievance related services are offered on behalf of selected client departments only. GCC-K is a single window IT-enabled facility. Envisaged as a Government to Citizen (G2C) interface, the Contact Centre enables the quick dissemination of critical Government Information, which is inaccessible or for the citizens.</p>
                                                        <p> The Contact Centre which is under the administration of Kerala State IT Mission, functions on all calendar days except Government declared Holidays/Regional holidays.</p>
                                                        <p> &nbsp;</p>
                                                        <p> The salient features of GCC-K are:</p>
                                                        <p> &nbsp;</p>
                                                        <ul style="margin-left:40px;">
                                                            <li class="listyle"> Bilingual support: Malayalam and English</li>
                                                            <li class="listyle"> Round the clock services (except public holidays) by trained Contact Centre Executives, with the support of a group dialing system consisting 30 lines</li>
                                                            <li class="listyle"> Serves more than 66 Clients including Government departments, organizations, projects etc.</li>
                                                            <li class="listyle"> Act as a Knowledge Database of Government Services</li>
                                                            <li class="listyle"> An average of 800 – 1000 calls, per day</li>
                                                            <li class="listyle"> Access from anywhere in Kerala without any queue and delay</li>
                                                        </ul>
                                                        <p> &nbsp;</p>
                                                        <p> Over the recent years, an increasing need has been felt within the Government for improving the quality of service being provided to the citizen, through GCC-K. On 16<sup>th</sup> February 2019, the revamped GCC-K has been commissioned to transform the existing voice-based Contact Center setup into a state-of-the-art Contact Centre, providing multi service delivery channels as e-mail, social media, SMS etc. along with conventional voice enhanced by VoIP technologies. Apart from these, integration of service delivery channels such as web portal, online chat, Mobile App etc. are in progress.</p>
                                                        <p> &nbsp;</p>
                                                        <p> The revamped Contact Centre provides the following facilities:</p>
                                                        <p> &nbsp;</p>
                                                        <ul style="margin-left:40px;">
                                                            <li class="listyle"> Multi channel support by leveraging the State-of-the-Art IT solutions</li>
                                                            <li class="listyle"> Customer Relationship Management</li>
                                                            <li class="listyle"> Interactive Voice Response System</li>
                                                            <li class="listyle"> Call Recording System</li>
                                                            <li class="listyle"> Campaign Management etc.</li>
                                                        </ul>
                                                        <p> &nbsp;</p>
                                                        <p> <strong>Our Mission</strong></p>
                                                        <p> &nbsp;</p>
                                                        <ul style="margin-left:40px;">
                                                            <li class="listyle"> To provide the citizens, with the information related to the Government services effectively and efficiently</li>
                                                            <li class="listyle"> To work as a ‘One-Stop' shop for the Government information and services</li>
                                                            <li class="listyle"> To provide access to the updated and accurate information</li>
                                                        </ul>
                                                        <p> &nbsp;</p>
                                                        <p> <strong>Our Vision</strong></p>
                                                        <p> &nbsp;</p>
                                                        <ul style="margin-left:40px;">
                                                            <li class="listyle"> Translate improved service delivery into cost savings</li>
                                                            <li class="listyle"> Increase the awareness of citizen call centre among the public</li>
                                                        </ul> <br>
